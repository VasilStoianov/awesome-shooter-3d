typedef struct Cube {

 mat4f translate;
 mat4f scale;
 mat4f rotateY;
 mat4f rotateX;
 mat4f rotate;
 mat4f transform;


} Cube;


void identityCubeMatrices(Cube *cube){
    identity(&cube->rotate);
    identity(&cube->scale);
    identity(&cube->translate);
    identity(&cube->transform);
    identity(&cube->rotateX);
    identity(&cube->rotateY);
}